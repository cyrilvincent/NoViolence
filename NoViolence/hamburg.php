      <p>
        <b>Agir pour la Paix : Carnet de voyage à Hamburg</b>
      </p>
      <p>
        Jour 1<br/>
        1263 km. Telle est la distance parcourue ce dimanche 17 mai 2015 entre Grenoble et Hambourg.<br/>
        Distance couverte sous un soleil doux et dans une ambiance sereine, teintée d'humour et d'amour. On aurait dit une équipe d'acteur marchant sur un tapis rouge, la tête haute et l'esprit requinqué par l'envie de partage des merveilles de cette beauté de la mutualisation des singularités positives. Cette équipe d'acteur est constituée de dix-huit personnes qui, durant une semaine seront les ambassadeurs de tous les autres membres des Ateliers Agir Pour la Paix.
      </p>
      <p>
        Quelques imprévus et reflexes de départ<br/>
        6h, telle était l'heure arrêtée pour le départ, lors du dernier atelier de finalisation du voyage. C'était sans compter avec la mise en circulation tardive des transports en commun le dimanche. Malgré cela, une valeur a émergé la solidarité agissante.<br/>
        Il s'en est suivi deux oublis d'éléments d'identification officielle de deux acteurs du groupe. Là encore, on a pu observer une double valeur : la patience et la compréhension de l'autre, le tout dans un fou rire.<br/>
        Le bon réflexe : stationnés dans une station de service à la sortie de Grenoble, les deux trafics vont faire l'objet d'un dernier contrôle. Mise à niveau des pneus, contrôle du niveau d'huile...
      </p>
      <p>
        Quelques scènes cocasses<br/>
        Premier arrêt après trois cent kilomètres. Pour certains, on fait une pause « pipi », pour d'autres on se dégourdi les jambes et pour d'autres quelques commentaires d'ambiance de chaque trafic. Côté ambiance, chaque trafic a sa recette. Entre recherche de station radios de musiques, sommeil avec des voisins transformés pour l'occasion en té d'oreiller, et lecture pour l'un des trafics. L'autre quant à lui a plus été absorbé par un genre musical dont seul avait le secret le chauffeur.<br/>
        Avant de reprendre le chemin, un des acteurs a oublié sa tablette sur un bac à ordures. Sur le chemin, cet acteur est interpellé par un autre qui voulait utiliser cette tablette pour mettre de la musique dans la voiture. Sûre de lui, il répond que son bijou électronique est entre les mains d'un autre qui lui, se trouve dans l'autre voiture. Pour avoir le coeur net, un troisième acteur demande à ce que l'on appelle pour se rassurer. La réponse ne s'est pas fait attendre. Il n'a pas la tablette. Il est 11h25, à cet instant un profond silence saisi le groupe. Le propriétaire de la tablette fini par prononcer la phrase que tout le monde redoutait: « je pense que j'ai oublié ma tablette lorsqu'on s'est arrêté ». Dix kilomètres plus loin, la température de ce dernier augmente, sa peau blanchi, il n'en peut plus au point d'avoir des remontés gastriques.<br/>
        Nous sommes obligés de nous arrêter une seconde fois. Le propriétaire de la tablette descend du véhicule. Il s'éloigne dans un champ à perte de vue. Il faut qu'il provoque les vomissures. Quinze minutes plus tard, il est soulagé. Tout le monde est à ses côtés pour s'en quérir de son état. Par son calme et sa sérénité, on comprend qu'il veut nous dire, comme le titre de l'ouvrage de Pascal Boniface et Médine, « Don't Panik ».<br/>
        Dans l'autre véhicule, un débat a été ouvert sur la religion. Les croyants et les non-croyants, en toute convivialité, ont échangé sur leurs positions de foi et de non-foi à une force supérieure. De l'avis de certains acteurs et actrices, cet échange a, non seulement, contribué à reconfigurer leurs perceptions les uns vis-à-vis des autres. Et de l'autre de renforcer les liens de fraternités.<br/>
      </p>
      <p>
        Quand le changement de langue nous fait des tours<br/>
        « Nous sommes à l'aire de repos d'Ausfahrt » : voilà la première réponse qu'un des acteurs du véhicule de tête donne comme repéré au second véhicule. Il faut dire que nous étions bien chanceux. Cette aire de repos se situait bien après le premier Ausfahrt. C'est en reprenant la route que l'on s'aperçoit qu'il y'a plusieurs Ausfahrt. Je vous laisse imaginer la suite....<br/>
        « On vous attend à Rasthof » : Comme la précédente réponse, Rasthof était considéré comme un nom de ville ou commune et non comme un lieu où on pouvait manger. La chance du débutant nous a encore sourit. Cet arrêt aux environs de 12h50 était la pause déjeuné. Après une heure trente passé à cet endroit, nous avons repris la route.
      </p>
      <p>
        L'arrivée à Hamburg<br/>
        C'est aux alentours de 21h45 que nous sommes arrivés à l'hôtel où nous devions passer la nuit.<br/>
        Deux actrices vont principalement nous être utiles et être incontournables. Comme l'a précisé quelqu'un, si elles n'avaient pas été là, ça aurait été un peu compliqué surtout après « Ausfahrt et Rasthof ».<br/>
        Malgré ce temps long, la bonne humeur n'a pas fui les acteurs et actrices du groupe. Nous nous sommes rendus dans un Kébab non loin de l'hôtel pour nous restaurer et prendre, enfin, un repos mérité avant de poursuivre pour Copenhague au Danemark.<br/>
        Notons pour finir ce premier carnet de voyage, que certaines personnes ont fait le constat suivant : « Depuis que nous sommes entrés en Allemagne, le regard des personnes sur nous est très insistant ». Une série de questions pourraient être posées:<br/>
        1. Comment sont nous perçus par ces personnes?<br/>
        2. Quels contenus ou quelles explications pouvons-nous tenter de faire?<br/>
        3. Est-ce qu'on pourra être pertinents dans nos analyses en nous basant uniquement sur ce regard?<br/>
        4. Qu'est-ce que ça nous fait d'être autant regardé?<br/>
        Ces questions nous font utilement penser à ce jeu sur les représentations que nous avions fait lors d'un atelier et qui nous sert de méthode de présentation des nouveaux au sein des Ateliers « Agir pour la paix ».<br/>
        Un constat partagé des deux trafics, les changements de place se faisaient de façon naturelle. Changements qui n'impactaient aucunement sur la bonne ambiance dans les trafics.<br/>
        Terminons quand même cette journée par une note positive. La tablette oublié a été prise par un autre dans l'optique de faire passer un message. Lequel????????<br/>
      </p>
      <p>Jour 3</p>
      <p>Le Conseil des jeunes de la Commune d'Ishoj qui accueille est situé à une vingtaine de kilomètres de
Copenhague. La réunion d'évaluation et de programmation de la journée fait la veille nous a été
bénéfique en termes d'organisation de la journée. Des sous-groupes spécifiques ont été mis en place.
A savoir, un pour les courses, un autre pour la création de la page facebook, pour alimenter en
photos le séjour et la décision de veiller au rangement et à la propreté des locaux du Conseil des
Jeunes.
Les acteurs qui se sont désignés spontanément la veille pour les courses, vous vous doutez bien qu'ils
n'ont pas tous répondu présent au réveil. Ce qui n'a pas empêché ceux déjà levés d'aller remplir la
mission au nom du groupe. Le beau qu'il faut signaler est l'expression de désolation exprimée par
ceux qui n'ont pas réussi à prendre le dessus sur le petit sommeil doux du matin.
Pour le petit déjeuner, il s'est déroulé en toute convivialité avec un souci de veiller à ce que tout le
groupe déjeune. Observer cette volonté forte de construire un nouveau répertoire d'habitudes,
propres au groupe est en marche. Ce qui a constitué le fou rire du matin, c'est l'absence de lait. En
effet, ceux qui ont les courses, croyant avoir pris du lait, ont plutôt sélectionné du yaourt. Cette
confusion est venu du fait qu'ils n'ont pas réussi à déchiffrer la langue danoise. Face à cette absence,
un acteur de dire, mais « mon frère, vous ne connaissez pas dire lait en anglais ». Et pour avoir le
coeur le net, ce dernier va demander à un de nos hôtes qui venaient s'enquérir sur notre nuit, s'ils ont
du lait. Et dans sa question, il croyait prononcer lait en anglais, mais l'interlocuteur n'a pas compris et
lui a encore servi un autre bric de lait. Je vous laisse imaginer la suite...
Aux environs de 10h10, nous avons pris la route pour Copenhague, la ville du vélo. Les trafics ont été
stationnés à une heure de marche du lieu à observer, la synagogue. Une fois descendu ses trafics
avec nos pancartes, nous avons suscité la curiosité des passants, tant en vélos, à pied qu'en voiture.
Au point où, une dame s'est arrêtée et nous a demandé ce qu'on revendique. Un acteur qui avait pris
soin de construire une question ouverte en référence à son thème l'a montré à la dame. Et cette
question était : « Pensez-vous qu'il existe de la justice (sociale) au Danemark? ». Sans ambages, elle a
répondu non. « Qu'ils y'a ceux qui sont égaux et ceux qui sont plus qu'égaux ».
Sur la route de la synagogue, chaque groupe thématique allait recueillir les paroles des passants. Ce
qui n'a pas toujours été facile et n'a entamé en rien la témérité des acteurs en pérégrination de
compréhension sur le comment faire société. Sur le chemin, Matthias, l'ancien stagiaire de Modus
Operandi, a allié travail et tourisme. C'est ainsi que nous nous sommes rendus au palais royal.
Subjugué et non déconcentré sur l'objectif du voyage, certains acteurs se rapproché de la garde
royale. On aurait dit, pour certains, que se sont des statuettes. Un de ces gardes voyants que les
acteurs se rapprochaient trop de lui pour des prises de vues, a soulevé son arme et enlever l'écran de
sécurité. Fous rire, courses et waouuuuuuuuuuuuuu.
Après une heure de marche, le tout sous un temps pas très clément et alternatif, pluie, froid, soleil
nous sommes arrivés à la synagogue. N'ayant pas pu avoir de rendez-vous, la visite a juste consisté à
l'observation. A côté de la synagogue, il y'avait un restaurant. Il fallait recharger les batteries avant
de reprendre la marche à pied pour le centre de ressources de jeunes d'un quartier qualifié de chaud.
Cependant, certains acteurs n'ont pas hésité à aller à la rencontre des forces de l'ordre bien équipé
qui gardaient le lieu de culte. Bien que peu disert, un des agents des forces de l'ordre a juste précisé :
« c'est bien ce que vous faites. Partir de France pour faire le tour de certaines villes d'Europe pour
voir comment elles font société est une initiative à multiplier ».
Le déjeuner dans le restaurant dénommé Ankara était simplement waouuuuuuuuuuuuuu. Je vous
laisse imaginer la suite... Aux environs de 14h27, sans sieste il fallait reprendre la marche à pied pour
le centre de ressources de jeunes. Temps prévu à pied 40mn. L'heure du rendez-vous étant prévu
pour 17h, l'exercice du recueil de paroles s'est poursuivi dans la ville de Copenhague. A la traversée
d'un pont, une actrice pose la question, quelle différence faites-vous entre les deux visages à la Janus
de ces deux face de la ville séparée par ledit pont? Les acteurs présents ont tous, comme en coeur,
donnés la même réponse: « là on sent très bien que l'on entre dans un quartier à dominance
étrangère ». Qu'est-ce qui vous le fait dire? Je vous laisse le soin d'imaginer la réponse. Car,
effectivement le quartier que nous traversons est en situation de dominance de populations
identifiées comme issues de l'immigration.
Dans cette marche qui ne voulait plus se terminer, le GPS que nous utilisons a été victime de
surmenage et le médecin prénommé fatigue a été sans appel: « un repos s'impose ». Que faire?
Heureusement que les NTICs sont là pour nous rappeler qu'ils peuvent nous être utiles. C'est alors
qu'une actrice a appelé notre interlocuteur afin de la situer où nous nous trouvions.
Une fois au centre de ressources de jeune, l'accueil a encore été waouuuuuuuuuuuuuu. Pour ces
personnes que nous ne connaissions pas et que nous rencontrions physiquement pour la première
fois, il a été bon de constater que l'acceptation de la différence dans le respect est une valeur
importante sur la problématique du comment faire société. Après les civilités d'usage, le groupe
d'acteurs et d'actrices a été convié à une visite du centre. C'était une fois encore
waouuuuuuuuuuuuuu. Que dire de ces grands écrans plats d'ordinateurs. Au point où, un acteur du
groupe a demandé ce qu'on prenne de ces écrans pour montrer à qui de droit. Et de conclure, je
pense que je vais m'installer ici.
Après la visite du centre, on a été convié à la fête des pizza, plus que XXL avant le débat prévu autour
de la place de l'islam dans la société. Avant le lancement du débat, il a été demandé au groupe
d'acteurs et d'actrices de partager avec l'assistance les raisons de notre présence. Ce fut un moment
grave empreint d'émotion qui tire sa source sur la présentation faite par une actrice, lorsqu'elle a
remonté la genèse en parlant de l'assassinat de jeunes sans histoires, situés dans une voie d'avenir,
par un simple regard. Le temps s'est arrêté, on aurait pu entendre une mouche volée, tellement,
l'assistance a vu son esprit être en total connexion avec cette actrice. Qui, elle-même, n'a pas pu
continuer en laissant l'acteur qui l'accompagnait poursuivre. Cet acteur a donc précisé en fin que ce
projet sur le comment faire société était pour le groupe d'aller à la rencontre d'autres sociétés pour
observer les dynamiques, les expériences collectives relatives à la volonté de construire un
imaginaire commun.
Place au débat sur le thème pré-évoqué. Ce débat a été structuré en deux parties. Une sur les
problèmes rencontrés dans la société en rapport avec la religion musulmane et l'autre sur les
solutions proposées relatives aux problèmes posés. Les échanges ont été riches sur le sujet. Le
constat que l'on pourrait dégager se résumerait en cette problématique : comment à partir d'un
constat (le problème), le citoyen fuit l'enferment du simple constat pour imaginer ou inventer des
alternatives qui contribueraient à promouvoir la valeur de la différence dans le respect.
A la fin du débat, nos hôtes nous ont fait une visite guidée de leur quartier. Une phrase a retenu
l'attention, « l'architecture que je vois ici ne reflète pas celle des quartiers populaires, car chez-nous
vous verrez de grosses tours d'immeubles où sont parqués les gens. ». Un sujet de travail a été tout
trouvé. Et l'acteur qui a prononcé cette phrase a décidé de faire un travail, à partir des observations
de ce voyage, sur le thème « urbanisation et violence ». Par la suite, on a visité le premier lieu de
prière pour les musulmans. Une Mosquée toute neuve. Pour le guide qui nous accompagnait, « le
projet de Mosquée avait reçu l'accord favorable de l'Etat, mais une partie de la population n'était pas
favorable ». Avant de prendre congé de nos hôtes, nous leur avons invité à participer à la journée du
02 octobre 2015. La réponse ne s'est pas fait attendre. Le responsable a spontanément dit …...........
(à vous de deviner)
C'est aux environs de 23h45 minutes que nous sommes revenus dans la ville où nous séjournons.
Avant de prendre le chemin du lit. Une réunion d'évaluation a été rapidement faite sur la journée et
le programme du lendemain arrêté également.
