      <p>Jour 4</p>
      <p>

906 Km. Tel est la distance parcourue par le groupe d'acteurs et d'actrices le mercredi 20 mai 2015.
soit au total, en trois jours, une distance de 2484 km.
Au départ de Copenhague, une réunion s'est tenue entre le responsable du centre de jeune et une
équipe de deux actrices et un acteur du groupe. Un accord a été trouvé pour la présence des jeunes
du centre pour la journée du 02 octobre 2015. Arrivée dont le nombre sera conditionné par l'accord
des parents et des autorités locales. Il nous revient, d'organiser leur séjour à Grenoble (du jeudi 1er
octobre au dimanche 04).
Vous vous doutez bien que l'heure prévue la veille pour le départ n'a pas été respecté. Au lieu de
10h, nous avons quitté, sous un fond de tristesse, le centre de jeune de la Commune d'Isjoh. Il en est
de même des jeunes du centre qui ont été dégoutté de ne pas pouvoir nous dire au revoir, parce que
en cours. C'est aussi ça la rigueur scolaire, nous a-t-on dit.
Avant de prendre la route, un groupe d'acteurs a fait le tour des endroits où nous sommes passés,
question ne se rassurer que nous n'avions rien oublié. Si vous avez lu le premier carnet voyage, où
nous vous avons partagé l'oubli, par un des acteurs de sa tablette. Eh oui, ce dernier nous a fait, cette
fois-ci, le replay en trois temps de son téléphone portable. En effet, un des responsable, a rappelé
une première fois à cet acteur qu'il a oublié son téléphone. Ce dernier croyant l'avoir pris, l'a laissé
au même endroit. Et le responsable de revenir 15 minutes plus tard inviter l'acteur en question à
venir récupérer son téléphone. Cette fois-ci l'acteur l'a effectivement pris le téléphone. Eh oui,
comme dit l'adage, jamais deux sans trois, le célèbre acteur a récidivé. C'est encore le même
responsable qui, passant dans une pièce attenante à son bureau est tombé sur le même téléphone. Je
vous laisse imaginer la réaction de ce responsable....
C'est donc vers 11h15 que nous avons enfin pu prendre la route. Après une quinzaine de kilomètre,
les visages étaient marqués par la fatigue et comme toujours cette énergie enfouie au fond de nousmêmes,
nous rappelait, les raisons qui ont motivées ce voyage. Comme depuis le départ de
Grenoble, les acteurs en chef qui conduisent ont été plus qu'exceptionnels par leur dévouement au
service du groupe.
Comme nous avons pris le Ferry lors de notre trajet Hambourg – Copenhague, nous avons fait le
choix de régler le GPS afin qu'il évite le Ferry. Alors là, on a pas été déçu par la beauté du paysage,
de ses ponts en hauteur, de ces étendus d'eau à perte de vues. Cependant, les multiples travaux
rencontrés sur la route nous a considérablement ralenti dans notre voyage.
Une actrice nous a annoncé, à un moment du voyage, bienvenue aux Pays-Bas. Le pays situé en
deça du niveau de la mer et célèbre pour plus d'une chose dont je vous laisse le soin de deviner. A
peu près 25 kilomètres après notre entrée au Pays-Bas, une voiture de police nous est passé devant
et nous avons simplement vu apparaître un mot qui clignotait « Folgen ». Heureusement pour nous
qu'une des actrices avait une parfaite connaissance de la langue néerlandaise. Que serait-il arrivé si
elle n'avait pas été là, pour rappeler au chauffeur du premier de ne pas passer la voiture de police,
mais de la suivre? On a donc suivi cette voiture de police jusqu'à un parking relai situé sur le
chemin. Une fois stationné, les agents de police nous ont demandé de nous présenter. Passeports,
pour certains, carte d'identité pour d'autres et récépissé pour l'un.
Rappelez-vous, le premier carnet de voyage. Un acteur et une actrice avaient oublié leur passeport
au départ. Je vous laisse, une fois encore, imaginer ce qui aurait pu nous arriver si l'acteur en chef
n'avait veiller à vérifier que tous les autres avaient leurs pièces d'identification par devers-eux.
Après une dizaine de minute, l'actrice qui maîtrise langue, tel un tribun, a commencé à partager
avec ces agents les raisons de notre voyage. Ce qui a eu le mérite d'accélérer le contrôle des pièces
d'identification et de bénéficier à la fin des encouragements de ces agents de force de police. Avant
de reprendre la route, l'actrice à la maîtrise de la langue locale s'est exclamée:
« waouuuuuuuuuuuuuu, c'est la première fois que je me fais contrôler » et un autre acteur de
poursuivre, dans un fou rire, « marche avec moi et tu verras que tu seras contrôlée au moins une fois
par semaine ».
Nous avons par la suite bien roulé jusqu'à Amsterdam. Retrouver l'auberge où nous devions passer
la nuit a été un peu compliqué avec les travaux sur la route où était géographiquement situé
l'auberge. Après une trentaine de minute, l'équipe de deux acteurs et une actrice qui avait décidé de
trouver l'auberge à pied est revenu pour nous dire, c'est OK.
C'est donc vers 22h30 que nous avons cherché où nous restaurer avant de prendre le chemin du lit et
préparer nos différents nos corps qui commence à ployer sous le coup de la fatigue, à affronter la
journée suivante.
